#include <iostream>
using namespace std;

int main() {

    // Tanpa Looping
    cout << 1 << endl;
    cout << 2 << endl;
    cout << 3 << endl;
    cout << 4 << endl;
    cout << 5 << endl;
    cout << "-----------------" << endl;

    // Dengan Looping (For Loop)
    // digunakan untuk melakukan perulangan dengan jumlah yang sudah diketahui
    for (int i = 1; i <= 5; i++) {
        cout << i << endl;
    }
    cout << "-----------------" << endl;

    return 0;
}